#!/usr/bin/env python3
"""
Figure 6: QFT Incremental Rank Growth per Controlled Rotation
==============================================================
Three-panel staircase plot showing cumulative GF(2) rank vs
fractional T-gate position for N = 8, 16, 32 using Generator C
(exact angles, 3 non-Clifford gates per controlled rotation).

Uses: results/figure6_qft_incremental_genC.csv
      (generated by benchmarks/generate_figure6_data.jl)

Output:
  paper/figure6.pdf
  paper/figure6.png
"""

import matplotlib
matplotlib.use("Agg")
import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import matplotlib as mpl
from pathlib import Path

# ── Paths ──────────────────────────────────────────────────────────────────────
repo = Path(__file__).resolve().parent.parent
data_csv = repo / "results" / "figure6_qft_incremental_genC.csv"
paper_dir = repo / "paper"
paper_dir.mkdir(exist_ok=True)

# ── Publication-quality settings ──────────────────────────────────────────────
mpl.rcParams.update({
    "font.family": "serif",
    "font.serif": ["Computer Modern Roman", "DejaVu Serif", "Times New Roman"],
    "font.size": 12,
    "axes.labelsize": 14,
    "axes.titlesize": 15,
    "legend.fontsize": 10,
    "xtick.labelsize": 12,
    "ytick.labelsize": 12,
    "figure.dpi": 150,
    "savefig.dpi": 300,
    "savefig.bbox": "tight",
    "axes.linewidth": 1.0,
    "xtick.major.width": 0.8,
    "ytick.major.width": 0.8,
})

# ── Load data ──────────────────────────────────────────────────────────────────
df = pd.read_csv(data_csv)
print(f"Loaded {len(df)} incremental records")

n_vals = sorted(df["n_qubits"].unique())
print(f"N values: {n_vals}")

# Verify T-gate counts match 3N(N-1)/2
for n in n_vals:
    nd = df[df["n_qubits"] == n]
    t_total = len(nd)
    t_expected = 3 * n * (n - 1) // 2
    final_rank = nd["cumulative_rank"].iloc[-1]
    n_jumps = (nd["rank_delta"] > 0).sum()
    print(f"  N={n:2d}: t={t_total} (expected {t_expected}), "
          f"final rank={final_rank} (N-1={n-1}), rank jumps={n_jumps}")

# ── Colors ─────────────────────────────────────────────────────────────────────
n_colors = {8: "#1f77b4", 16: "#ff7f0e", 32: "#2ca02c"}

# ═══════════════════════════════════════════════════════════════════════════════
# FIGURE: Three-panel staircase
# ═══════════════════════════════════════════════════════════════════════════════

fig, axes = plt.subplots(1, len(n_vals), figsize=(5 * len(n_vals), 5),
                          sharey=True)
if len(n_vals) == 1:
    axes = [axes]

for i, n in enumerate(n_vals):
    ax = axes[i]
    nd = df[df["n_qubits"] == n].copy()
    color = n_colors.get(n, "#333333")
    t_max = nd["t_index"].max()

    # Normalize x to fractional position [0, 1]
    x_frac = nd["t_index"].values / t_max

    # Plot staircase line
    ax.plot(x_frac, nd["cumulative_rank"].values,
            color=color, linewidth=1.5, zorder=3)

    # Mark rank jumps with dots
    jumps = nd[nd["rank_delta"] > 0]
    ax.scatter(jumps["t_index"].values / t_max,
               jumps["cumulative_rank"].values,
               color=color, s=25, zorder=4)

    # Horizontal reference line at rank = N - 1
    ax.axhline(y=n - 1, color=color, linestyle=":", linewidth=0.8, alpha=0.4)

    # T-gate count in subtitle matches paper: 3N(N-1)/2
    t_count = 3 * n * (n - 1) // 2
    ax.set_xlabel("Fractional T-Gate Position")
    ax.set_title(f"$N = {n}$ ({t_count:,} non-Clifford gates)")
    ax.grid(True, alpha=0.15, linewidth=0.5)
    ax.set_xlim(0, 1.02)

axes[0].set_ylabel("Cumulative GF(2) Rank")
fig.suptitle("QFT: Incremental Rank Growth per Controlled Rotation",
             fontsize=15, y=1.02)

fig.tight_layout()

# ── Save ──────────────────────────────────────────────────────────────────────
out_pdf = paper_dir / "figure6.pdf"
out_png = paper_dir / "figure6.png"
fig.savefig(out_pdf)
fig.savefig(out_png)
print(f"\nSaved: {out_pdf}")
print(f"Saved: {out_png}")

