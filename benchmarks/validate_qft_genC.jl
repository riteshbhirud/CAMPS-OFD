QFT CAMPS Validation — Generator C (Exact Angles)
===================================================

Runs the full CAMPS OFD+OBD hybrid pipeline on QFT circuits generated by
qft_circuit() (Generator C, exact rotation angles).

This replaces the QFT row in Table 1, which previously used Generator A/B
(the incorrect T-gate approximation decomposition).

Pipeline (same as run_parallel_benchmark_complete.jl):
  For each non-Clifford gate:
    1. Try OFD (find_disentangling_qubit → build_disentangler → apply)
    2. If OFD fails → OBD fallback (apply_gate! with OBDStrategy)

Key difference from previous validation:
  - Circuit source: qft_circuit(N) instead of generate_circuit(QFTFamily(); ...)
  - Rotation angles: exact 2π/2^m (not all are π/4 T-gates)
  - Gate format: all CAMPS Gate objects (CliffordGate, RotationGate, CZGate, SWAPGate)

Usage:
    julia benchmarks/validate_qft_genC.jl [N_max]

    N_max = 4    → Only N=4 (timing test)
    N_max = 8    → N = 4, 6, 8
    N_max = 16   → N = 4, 6, 8, 10, 12, 14, 16

Output:
    results/validate_qft_genC.csv
=

camps_dir = dirname(dirname(@__FILE__))
println("Activating CAMPS.jl environment: $camps_dir")
using Pkg
Pkg.activate(camps_dir)

using CAMPS
using QuantumClifford
using Random
using Printf
using Dates

"""
    run_qft_genC_validation(n_qubits::Int) -> NamedTuple

Run full CAMPS OFD+OBD hybrid simulation on a QFT circuit generated by
qft_circuit(N) (Generator C, exact angles).

Uses the EXACT same gate-application logic as run_parallel_benchmark_complete.jl:
  - CliffordGate → apply_gate!(state, gate)
  - Non-Clifford RotationGate → try OFD first, OBD fallback on failure
  - Other gates (CZGate, SWAPGate) → apply_gate!(state, gate)

The only difference from the existing validation is:
  - OFD success/failure is manually tracked for ALL non-Clifford RotationGates
    (not just those with angle = π/4), since Generator C uses exact angles.
"""
function run_qft_genC_validation(n_qubits::Int)
    circuit = qft_circuit(n_qubits)

    state = CAMPSState(n_qubits; max_bond=2048)
    initialize!(state)

    ofd_success = 0
    ofd_fail = 0
    n_clifford = 0
    n_non_clifford = 0

    start_time = time()

    for gate in circuit
        if gate isa CliffordGate
            apply_gate!(state, gate)
            n_clifford += 1

        elseif gate isa RotationGate
            if is_clifford_angle(gate.angle)
                apply_gate!(state, gate)
                n_clifford += 1
            else
                n_non_clifford += 1

                P_twisted = compute_twisted_pauli(state, gate.axis, gate.qubit)
                control = find_disentangling_qubit(P_twisted, state.free_qubits)

                if control !== nothing
                    ofd_success += 1
                    apply_gate!(state, gate; strategy=HybridStrategy())
                else
                    ofd_fail += 1
                    apply_gate!(state, gate; strategy=HybridStrategy())
                end
            end
        else
            apply_gate!(state, gate)
            n_clifford += 1
        end
    end

    runtime = time() - start_time

    final_chi = get_bond_dimension(state)
    final_S2 = max_entanglement_entropy(state.mps)

    total_non_cliff = ofd_success + ofd_fail
    ofd_rate = total_non_cliff > 0 ? ofd_success / total_non_cliff : NaN

    gf2_rank = get_predicted_bond_dimension(state)

    return (
        n_qubits = n_qubits,
        n_total_gates = length(circuit),
        n_clifford = n_clifford,
        n_non_clifford = n_non_clifford,
        t_count = total_non_cliff,
        ofd_success = ofd_success,
        ofd_fail = ofd_fail,
        ofd_rate = ofd_rate,
        final_chi = final_chi,
        final_S2 = final_S2,
        runtime = runtime
    )
end

function main()
    n_max = length(ARGS) >= 1 ? parse(Int, ARGS[1]) : 4

    if n_max <= 4
        n_range = [4]
    elseif n_max <= 8
        n_range = [4, 6, 8]
    else
        n_range = [4, 6, 8, 10, 12, 14, 16]
    end

    println("="^90)
    println("QFT CAMPS VALIDATION — GENERATOR C (EXACT ANGLES)")
    println("="^90)
    println("Generator: qft_circuit() from simulation.jl")
    println("Strategy:  HybridStrategy (OFD first, OBD fallback)")
    println("N range:   $(n_range)")
    println("Date:      $(Dates.format(now(), "yyyy-mm-dd HH:MM:SS"))")
    println()

    results = NamedTuple[]

    println("-"^90)
    @printf("%4s %6s %6s %6s %6s %8s %8s %8s %8s %10s\n",
            "N", "gates", "cliff", "non-C", "t", "OFD ok", "OFD fail", "OFD%", "χ", "time")
    println("-"^90)

    for n in n_range
        result = run_qft_genC_validation(n)
        push!(results, result)

        @printf("%4d %6d %6d %6d %6d %8d %8d %7.1f%% %8d %8.1fs\n",
                result.n_qubits,
                result.n_total_gates,
                result.n_clifford,
                result.n_non_clifford,
                result.t_count,
                result.ofd_success,
                result.ofd_fail,
                result.ofd_rate * 100,
                result.final_chi,
                result.runtime)
    end

    println("-"^90)
    println()

    println("="^90)
    println("SUMMARY")
    println("="^90)
    println()

    for r in results
        @printf("N=%d:\n", r.n_qubits)
        @printf("  Circuit: %d gates (%d Clifford, %d non-Clifford)\n",
                r.n_total_gates, r.n_clifford, r.n_non_clifford)
        @printf("  OFD: %d/%d succeeded (%.1f%%)\n",
                r.ofd_success, r.t_count, r.ofd_rate * 100)
        @printf("  OBD fallbacks: %d\n", r.ofd_fail)
        @printf("  Final bond dimension: χ = %d\n", r.final_chi)
        @printf("  Max entanglement entropy: S₂ = %.4f\n", r.final_S2)
        @printf("  Runtime: %.1f s\n\n", r.runtime)
    end

    output_dir = joinpath(camps_dir, "results")
    mkpath(output_dir)
    csv_path = joinpath(output_dir, "validate_qft_genC.csv")

    open(csv_path, "w") do io
        println(io, "n_qubits,n_total_gates,n_clifford,n_non_clifford,t_count,ofd_success,ofd_fail,ofd_rate,final_chi,final_S2,runtime")
        for r in results
            @printf(io, "%d,%d,%d,%d,%d,%d,%d,%.6f,%d,%.6f,%.2f\n",
                    r.n_qubits, r.n_total_gates, r.n_clifford, r.n_non_clifford,
                    r.t_count, r.ofd_success, r.ofd_fail, r.ofd_rate,
                    r.final_chi, r.final_S2, r.runtime)
        end
    end
    println("Results saved to: $csv_path")

    println("\n" * "="^90)
    println("VALIDATION COMPLETE")
    println("="^90)

    return results
end

results = main()
